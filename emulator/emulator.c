void initialize(struct cpu *instance)
{
	instance->pc = 0x200;
	instance->opcode = 0;
	instance->index_reg = 0;
	instance->stackp = 0;
	for(int i = 0; i<80; i++)
	{
		instance->memory[i] = instance->chip8_fontset[i];
	}
	
}
